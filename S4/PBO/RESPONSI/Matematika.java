
public class Matematika {

    public static int getTambah( int number1, int number2 ) {
        return number1 + number2;
    }

    public static int getKali(int number1, int number2) {
        return number1 * number2;
    }

    public static void main(String[] args) {
        int tampilkanTambah = getTambah(2, 3);
        System.out.println(tampilkanTambah);

        int tampilkanKali = getTambah(2, 3);
        System.out.println(tampilkanKali);
    }
}